#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	while (*s != '\0' && *s != (c % 256))
		s++;
	if (*s == '\0' && (c % 256) != '\0')
		return (0);
	else
		return ((char *)s);
}
